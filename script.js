document.addEventListener('DOMContentLoaded', () => {

  // â”€â”€ Navigation scroll effect â”€â”€
  const nav = document.getElementById('nav');
  if (nav) {
    window.addEventListener('scroll', () => {
      nav.classList.toggle('nav--scrolled', window.scrollY > 40);
    });
  }

  // â”€â”€ Mobile hamburger menu â”€â”€
  const hamburger = document.getElementById('hamburger');
  const mobileMenu = document.getElementById('mobile-menu');
  if (hamburger && mobileMenu) {
    hamburger.addEventListener('click', () => {
      hamburger.classList.toggle('active');
      mobileMenu.classList.toggle('active');
    });
    // Close on link click
    mobileMenu.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', () => {
        hamburger.classList.remove('active');
        mobileMenu.classList.remove('active');
      });
    });
  }

  // â”€â”€ Scroll-triggered animations â”€â”€
  const observerOptions = {
    threshold: 0.12,
    rootMargin: '0px 0px -40px 0px'
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, observerOptions);

  document.querySelectorAll('.fade-in, .fade-in-left, .fade-in-right').forEach(el => {
    observer.observe(el);
  });

  // â”€â”€ FAQ Accordion â”€â”€
  document.querySelectorAll('.faq-item__question').forEach(btn => {
    btn.addEventListener('click', () => {
      const item = btn.parentElement;
      const isActive = item.classList.contains('active');

      // Close all
      document.querySelectorAll('.faq-item').forEach(faq => {
        faq.classList.remove('active');
      });

      // Open clicked (if it wasn't already active)
      if (!isActive) {
        item.classList.add('active');
      }
    });
  });

  // â”€â”€ Back to Top button â”€â”€
  const backToTop = document.getElementById('back-to-top');
  if (backToTop) {
    window.addEventListener('scroll', () => {
      backToTop.classList.toggle('visible', window.scrollY > 500);
    });
    backToTop.addEventListener('click', () => {
      window.scrollTo({ top: 0, behavior: 'smooth' });
    });
  }

  // â”€â”€ Modal handling â”€â”€
  const modalOverlay = document.getElementById('modal-apply');
  if (modalOverlay) {
    // Open modal buttons
    document.querySelectorAll('[data-modal="apply"]').forEach(btn => {
      btn.addEventListener('click', (e) => {
        e.preventDefault();
        modalOverlay.classList.add('active');
        document.body.style.overflow = 'hidden';
      });
    });

    // Close modal
    const closeBtn = modalOverlay.querySelector('.modal__close');
    if (closeBtn) {
      closeBtn.addEventListener('click', () => {
        modalOverlay.classList.remove('active');
        document.body.style.overflow = '';
      });
    }

    // Close on overlay click
    modalOverlay.addEventListener('click', (e) => {
      if (e.target === modalOverlay) {
        modalOverlay.classList.remove('active');
        document.body.style.overflow = '';
      }
    });

    // Close on Escape key
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && modalOverlay.classList.contains('active')) {
        modalOverlay.classList.remove('active');
        document.body.style.overflow = '';
      }
    });
  }

  // â”€â”€ Form submission (demo) â”€â”€
  const forms = document.querySelectorAll('form');
  forms.forEach(form => {
    form.addEventListener('submit', (e) => {
      e.preventDefault();
      const submitBtn = form.querySelector('[type="submit"]');
      const originalText = submitBtn.innerHTML;
      submitBtn.innerHTML = 'âœ“ Sent! We\'ll be in touch ðŸŽ‰';
      submitBtn.style.background = 'var(--clr-sage)';
      submitBtn.disabled = true;

      setTimeout(() => {
        submitBtn.innerHTML = originalText;
        submitBtn.style.background = '';
        submitBtn.disabled = false;
        form.reset();
        // Close modal if inside one
        const modal = form.closest('.modal-overlay');
        if (modal) {
          modal.classList.remove('active');
          document.body.style.overflow = '';
        }
      }, 3000);
    });
  });

  // â”€â”€ Filter buttons (listings page) â”€â”€
  const filterBtns = document.querySelectorAll('.filter-btn');
  if (filterBtns.length) {
    filterBtns.forEach(btn => {
      btn.addEventListener('click', () => {
        // Update active state
        filterBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');

        const filter = btn.dataset.filter;
        const cards = document.querySelectorAll('.room-card');

        cards.forEach(card => {
          if (filter === 'all' || card.dataset.area === filter || card.dataset.type === filter) {
            card.style.display = '';
            // Re-trigger animation
            card.classList.remove('visible');
            requestAnimationFrame(() => {
              card.classList.add('fade-in');
              requestAnimationFrame(() => {
                card.classList.add('visible');
              });
            });
          } else {
            card.style.display = 'none';
          }
        });
      });
    });
  }

  // â”€â”€ Image gallery for room detail pages â”€â”€
  const galleryThumbs = document.querySelectorAll('.room-detail__gallery-thumb');
  const galleryMain = document.querySelector('.room-detail__gallery-main img');
  if (galleryThumbs.length && galleryMain) {
    galleryThumbs.forEach(thumb => {
      thumb.addEventListener('click', () => {
        const newSrc = thumb.querySelector('img').src;
        const oldSrc = galleryMain.src;
        galleryMain.src = newSrc;
        thumb.querySelector('img').src = oldSrc;
      });
    });
  }

  // â”€â”€ Smooth scroll for anchor links â”€â”€
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', (e) => {
      const targetId = anchor.getAttribute('href');
      if (targetId === '#') return;
      const target = document.querySelector(targetId);
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });

  // â”€â”€ Parallax subtle effect on hero shapes â”€â”€
  const shapes = document.querySelectorAll('.hero__shape');
  if (shapes.length) {
    window.addEventListener('scroll', () => {
      const scrollY = window.scrollY;
      shapes.forEach((shape, i) => {
        const speed = (i + 1) * 0.3;
        shape.style.transform = `translateY(${scrollY * speed}px)`;
      });
    });
  }

});
